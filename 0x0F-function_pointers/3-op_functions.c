#include "3-calc.h"

/**
*op_add - adds
* @a: int value
* @b: int value
* Return: addition
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
*op_sub - subtarcts
* @a: int value
* @b: int value
* Return: subtraction
*/

int op_sub(int a, int b)
{
return (a - b);
}

/**
*op_mul- multiply
* @a: int value
* @b: int value
* Return: multiplication
*/
int op_mul(int a, int b)
{
return (a * b);
}

/**
*op_div - divide
* @a: int value
* @b: int value
* Return: divsion
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
*op_mod - modulus
* @a: int value
* @b: int value
* Return: modulus
*/
int op_mod(int a, int b)
{
return ((a % b));
}
